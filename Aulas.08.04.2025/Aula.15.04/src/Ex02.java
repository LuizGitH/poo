public class Ex02 {
    public static void main(String[] args) {
        double num_1 = 7.5;
        double num_2 = 8.0;
        double num_3 = 9.2;

        double media = (num_1 + num_2 + num_3) / 3;

        System.out.println("Nota 1: " + num_1);
        System.out.println("Nota 2: " + num_2);
        System.out.println("Nota 3: " + num_3);

        System.out.println("Média Aritmética: " + media);
    }
}
